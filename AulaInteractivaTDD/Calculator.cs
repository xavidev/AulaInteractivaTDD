using System;
namespace AulaInteractivaTDD
{
    public class Calculator
    {

       public Calculator()
        {

        }

        public int Add(int v1, int v2)
        {
            int result = v1 + v2;
            return result;
        }

        public int Substract(int v1, int v2)
        {
            int result = v1 - v2;
            return result;
        }


